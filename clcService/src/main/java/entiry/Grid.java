package entiry;

public class Grid {

    private int grid_number;
    private int merchandise_id;
    private int grid_status;

    public int getGrid_number() {
        return grid_number;
    }

    public void setGrid_number(int grid_number) {
        this.grid_number = grid_number;
    }

    public int getMerchandise_id() {
        return merchandise_id;
    }

    public void setMerchandise_id(int merchandise_id) {
        this.merchandise_id = merchandise_id;
    }

    public int getGrid_status() {
        return grid_status;
    }

    public void setGrid_status(int grid_status) {
        this.grid_status = grid_status;
    }
}
